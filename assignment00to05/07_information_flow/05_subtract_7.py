def subtract_seven(num: int) -> int:
    """
    Subtracts 7 from the given number.
    """
    return num - 7

def main():
    num = 7  # Initialize num to 7
    num = subtract_seven(num)  # Call subtract_seven to subtract 7 from num
    print("This should be zero:", num)  # Print the result

# There is no need to edit code beyond this point
if __name__ == '__main__':
    main()
